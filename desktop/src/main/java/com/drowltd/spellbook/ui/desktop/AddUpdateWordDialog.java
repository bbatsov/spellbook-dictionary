    private Action getSelectLineAction() {
        Action[] action = translationTextArea.getActions();

        for (int i = 0; i < action.length; i++) {

            if (action[i].getValue(Action.NAME).equals(DefaultEditorKit.selectLineAction)) {
                selectLine = action[i];
            }

        }

        return selectLine;
    }
